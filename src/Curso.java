
public class Curso {

	private String curs;
	private String nombre;
	
	
	public Curso(String curs, String nombre) {
		this.curs = curs;
		this.nombre = nombre;
	}
	
	public String getCurs() {
		return curs;
	}
	public void setCurs(String curs) {
		this.curs = curs;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	
	
}
